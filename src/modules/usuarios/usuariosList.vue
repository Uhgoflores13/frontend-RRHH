<script>
export default {
  data: () => ({
    usuarios: [],
    selected: [],
    loading: false,
    filtros_seleccionados_aux: [],
    filtros_disponibles: [],
    usuarioModal: false,
    usuarioData: null,
    headers: [
      {
        text: "Correo electr贸nico",
        align: "start",
        value: "email",
      },
      {
        text: "Ultima conexi贸n",
        align: "start",
        value: "last_login",
      },
      {
        text: "Deshabilitado",
        align: "start",
        value: "is_suspended",
      },
      { text: "Accion", value: "accion", sortable: false, width: "100" },
    ],
  }),
  methods: {
    async filtrarPerfiles() {
      let filtros = {};
      this.filtros_seleccionados.forEach((filtro_selec) => {
        filtros[filtro_selec.filter_name] = filtro_selec.value;
      });
      await this.getUsuarios(filtros);
    },
    limpiarFiltros() {
      this.filtros_seleccionados_aux.forEach((filtro) => {
        filtro.value = null;
      });
      this.filtros_seleccionados_aux = [];
      this.getUsuarios();
    },
    async getUsuarios(filtros = null) {
      this.loading = true;
      const response = await this.http_client("/api/v1/users", filtros);
      this.usuarios = response?.data;
      this.loading = false;
    },
    editingUsuario(item) {
      let roles = [];
      let perfiles = [];
      if (item.Rols.length > 0) {
        roles = item.Rols.map((item) => {
          return item.id;
        });
      }
      if (item.Perfils.length > 0) {
        perfiles = item.Perfils.map((item) => {
          return item.id;
        });
      }
      localStorage.setItem(
        "editingUsuario",
        JSON.stringify({
          id: item.id,
          email: item.email,
          is_suspended: item.is_suspended,
          roles,
          perfiles,
        })
      );
      this.$router.push(`edit/${item.id}`);
    },
    showUsuarioData(item) {
      this.usuarioModal = true;
      this.usuarioData = item;
    },
    async deleteUsuarios() {
      const usuarios = this.selected.map((usuario) => {
        return usuario.id;
      });
      const response = await this.http_client(
        "/api/v1/users",
        { id: usuarios },
        "delete"
      );
      if (response?.status == 200 || response?.status == 201) {
        this.temporalAlert({
          show: true,
          message: "Se eliminaron los perfiles",
          type: "success",
        });
      }
      this.selected = [];
      this.getUsuarios();
    },
  },
  computed: {
    busquedas() {
      return this.filtros_seleccionados.filter((filtro) => {
        return { id: filtro.id, search: filtro.search, value: filtro.value };
      });
    },
    filtros() {
      return [
        {
          id: 0,
          nombre: "Correo",
          tipo: "text",
          value: null,
          filter_name: "email",
        },
        {
          id: 1,
          nombre: "Deshabilitado",
          tipo: "select",
          multiple: false,
          options: [
            { text: "Si", value: true },
            { text: "No", value: false },
          ],
          value: null,
          textfield: "text",
          valuefield: "value",
          filter_name: "deshabilitado",
        },
      ];
    },
    filtros_seleccionados() {
      return this.filtros_seleccionados_aux;
    },
  },
  async mounted() {
    this.filtros_disponibles = this.filtros;
    await this.getUsuarios();
  },
};
</script>
